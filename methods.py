def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(a, b):
    return a + b
def subtracao(a, b):
    return a - b
def divisao(a, b):
    return a / b
def multiplicacao(a, b):
    return a * b
def potencia(b):
    return b * b